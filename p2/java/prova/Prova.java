// oq é classe abstrata
// Uma classe abstrata em Java é uma classe que não pode ser instanciada, ou seja, você não pode criar objetos a partir dela diretamente. Ela é projetada para ser usada como uma classe base ou superclasse para outras classes mais específicas, fornecendo métodos e atributos comuns a todas as subclasses.

// A classe abstrata pode conter métodos abstratos, que são métodos sem implementação definida. Esses métodos são declarados com a palavra-chave "abstract" e devem ser implementados nas subclasses que herdam da classe abstrata. As subclasses podem estender a classe abstrata e herdar seus métodos e atributos, além de implementar os métodos abstratos.

// A classe abstrata pode ser usada para definir uma interface comum para um grupo de classes relacionadas, o que ajuda a garantir que as classes derivadas sejam consistentes em sua implementação e comportamento.

