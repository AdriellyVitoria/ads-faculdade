//     o que é Composição
// Composição é um conceito da programação orientada a objetos (POO) que se refere à capacidade de uma classe ter objetos de outras classes como seus membros ou campos de dados.

// Em outras palavras, a composição é a capacidade de uma classe utilizar outros objetos para implementar seu comportamento ou oferecer funcionalidades adicionais. Esses objetos são geralmente instanciados dentro da classe principal, como campos de dados ou variáveis ​​locais em seus métodos.

// Um exemplo de composição pode ser uma classe de computador que possui um objeto de placa-mãe, um objeto de processador e um objeto de memória RAM como seus membros. A classe de computador pode usar esses objetos para implementar seu comportamento e oferecer funcionalidades adicionais, como inicializar o sistema operacional, executar aplicativos e armazenar dados.

// A composição permite que as classes sejam construídas a partir de objetos menores e mais especializados, o que facilita o desenvolvimento de software orientado a objetos e torna o código mais modular, reutilizável e extensível.

